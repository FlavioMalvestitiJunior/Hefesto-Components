    private void hFPasswordField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_hFPasswordField1FocusLost
        hFPasswordField1.valida();
        loginButton.setEnabled((usuarioValidado && hFPasswordField1.valida()));
        if (loginButton.isEnabled()) {
            loginButton.grabFocus();
        }
    }//GEN-LAST:event_hFPasswordField1FocusLost

