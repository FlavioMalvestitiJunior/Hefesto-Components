    /**
     * Marca componente como contendo valores invalidos.
     *
     * @param comp
     */
    public void setError(JComponent comp) {
        for (Component cmp : comp.getComponents()) {
            cmp.setVisible(true);
        }
        comp.setBorder(new LineBorder(Color.red));
    }

    /**
     * Remove a marcação de erro.
     *
     * @param comp
     */
    public void removeErro(JComponent comp) {
        for (Component cmp : comp.getComponents()) {
            cmp.setVisible(false);
        }
        comp.setBorder(new LineBorder(Color.LIGHT_GRAY));
    }

    public void setOK(JComponent comp) {
        for (Component cmp : comp.getComponents()) {
            if (cmp instanceof JLabel) {
                Icon b = new ImageIcon(new ImageIcon(getClass().getResource("/imagens/checkmark.png").getPath()).getImage().getScaledInstance(16, 16, Image.SCALE_SMOOTH));
                ((JLabel) cmp).setIcon(b);
                cmp.setVisible(true);
            }
        }
        comp.setBorder(new LineBorder(Color.GREEN));
    }

    /**
     * Inicializa os campos de texto.
     *
     * @param comp
     */
    public void inicializaCampo(JComponent comp) {
        Icon b = new ImageIcon(new ImageIcon(getClass().getResource("/imagens/cancel.png").getPath()).getImage().getScaledInstance(16, 16, Image.SCALE_SMOOTH));
        JLabel bb = new JLabel(b);
        comp.setLayout(new BorderLayout());
        bb.setSize(comp.getHeight(), comp.getHeight());
        comp.add(bb, BorderLayout.EAST);
        bb.setVisible(false);
    }

    public void msgErro(String erro) {
        jLabel4.setText(erro);
    }

