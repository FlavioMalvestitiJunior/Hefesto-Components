    /**
     * busca os Feriados contisdos em um mes.
     *
     * @param ano ano dos Feriados
     * @param mes mes dos feriados
     * @return
     */
    public List<LocalDate> getFeriadosMes(int ano, int mes) {
        List<LocalDate> feriadosMes = new ArrayList<>();
        for (Map.Entry<LocalDate, String> entry : datasMarcadas.entrySet()) {
            if (entry.getKey().getYear() == ano && entry.getKey().getMonthOfYear() == mes) {
                feriadosMes.add(entry.getKey());
            }
        }
        return feriadosMes;
    }

    /**
     * busca os feriados contidos em um determinado ano.
     *
     * @param ano ano dos feriados
     * @return
     */
    public List<LocalDate> getFeriadosAno(int ano) {
        List<LocalDate> feriadosMes = new ArrayList<>();
        for (Map.Entry<LocalDate, String> entry : datasMarcadas.entrySet()) {
            if (entry.getKey().getYear() == ano) {
                feriadosMes.add(entry.getKey());
            }
        }
        return feriadosMes;
    }

