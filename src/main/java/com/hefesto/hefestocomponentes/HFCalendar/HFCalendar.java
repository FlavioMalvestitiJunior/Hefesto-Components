    /**
     * inicializa os Listners de Mouse dos Dias
     */
    private void inicializaDiasListners() {
        for (JLabel cmp : labels) {

            cmp.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent e) {
                    if (e.getClickCount() >= 2) {
                        if (data != null) {
                            HFCalendar.this.diaSelecionado = new LocalDate(anoData, mesData, Integer.parseInt(((JLabel) e.getComponent()).getText()));
                            HFCalendar.this.data.setDate(diaSelecionado.toDate());
                            HFCalendar.this.data = null;
                            if (menu != null) {
                                HFCalendar.this.menu.removeAll();
                                HFCalendar.this.menu.setVisible(false);
                                HFCalendar.this.menu = null;
                            }
                        }

                    } else {
                        if (labelSelecionada != null) {
                            labelSelecionada.getParent().setBackground(isDiaFeriado(diaSelecionado) ? feriadoColor : (diaSemana(diaSelecionado)) % 7 == 0 ? fdsColor :(diaSemana(diaSelecionado)+1) % 7 == 0 ? fdsColor: dsColor);
                            labelSelecionada.setForeground(textDesSelecionadoColor);
                        }
                        HFCalendar.this.diaSelecionado = new LocalDate(anoData, mesData, Integer.parseInt(((JLabel) e.getComponent()).getText()));
                        labelSelecionada = (JLabel) e.getComponent();
                        labelSelecionada.getParent().setBackground(selecionadoColor);
                        labelSelecionada.setForeground(textSelecionadoColor);
                        tmpColor = selecionadoColor;
                    }
                }

                @Override
                public void mouseEntered(MouseEvent e) {
                    HFCalendar.this.tmpColor = e.getComponent().getParent().getBackground();
                    e.getComponent().getParent().setBackground(rasterColor);
                }

                @Override
                public void mouseExited(MouseEvent e) {
                    e.getComponent().getParent().setBackground(HFCalendar.this.tmpColor);
                }

            });
        }
    }

    /**
     * troca os componentes do painel
     *
     * @param comp
     */
    private void painelDiasAtualiza(JComponent comp) {
        diasPainel.removeAll();
        comp.setSize(diasPainel.getSize().width, diasPainel.getSize().height);
        diasPainel.add(comp);
        comp.setVisible(true);
        diasPainel.updateUI();
        tmpColor = null;
    }

    /**
     * Converte o nome do mês em numero
     *
     * @param mesnome
     * @return
     */
    private int numeroMes(String mesnome) {
        switch (mesnome) {
            case "Janeiro":
                return 1;
            case "Fevereiro":
                return 2;
            case "Março":
                return 3;
            case "Abril":
                return 4;
            case "Maio":
                return 5;
            case "Junho":
                return 6;
            case "Julho":
                return 7;
            case "Agosto":
                return 8;
            case "Setembro":
                return 9;
            case "Outubro":
                return 10;
            case "Novembro":
                return 11;
            case "Dezembro":
                return 12;
        }
        return 1;
    }

