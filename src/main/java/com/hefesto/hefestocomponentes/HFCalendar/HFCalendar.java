    private int diaSemana(LocalDate date) {
        return date.dayOfWeek().get();
    }

    @SuppressWarnings("empty-statement")
    /**
     * Preenche os dias do calendario conforme a data passada.
     */
    private void preencheDia(LocalDate dat) {
        LocalDate dat2 = new LocalDate(dat.getYear(), dat.getMonthOfYear(), 1);
        ano.setText(dat2.year().getAsText());
        mes.setText(dat2.monthOfYear().getAsText());
        anoData = dat2.getYear();
        mesData = dat2.getMonthOfYear();
        int cont = diaSemana(dat2);
        int cont2 = 1;
        for (int i = 0; i < labels.length; i++) {
            labels[i].setText("");
            labels[i].setForeground(textDesSelecionadoColor);
            labels[i].getParent().setBackground((i) % 7 == 0 ? fdsColor :(i+1) % 7 == 0 ? fdsColor: dsColor);
            labels[i].setToolTipText("");

        }

        boolean semSelecao = true;
        int maxDay = dat2.dayOfMonth().withMaximumValue().getDayOfMonth();
        for (int i = 0; i < maxDay; i++) {
            labels[cont].setText(cont2 + "");

            if (datasMarcadas.containsKey(dat2)) {

                if (labels[cont] != null && dat2.compareTo(diaSelecionado) != 0) {
                    labels[cont].setForeground(textDesSelecionadoColor);
                    labels[cont].getParent().setBackground(feriadoColor);
                    labels[cont].setToolTipText(datasMarcadas.get(dat2));
                }
            }
            if (dat2.compareTo(diaSelecionado) == 0) {
                tmpColorClick = labels[cont].getParent().getBackground();
                labels[cont].getParent().setBackground(selecionadoColor);
                labelSelecionada = labels[cont];
                labelSelecionada.setForeground(textSelecionadoColor);
                semSelecao = false;
            }
            if (semSelecao) {
                if (labelSelecionada != null && dat2.compareTo(diaSelecionado) == 0) {
                    labelSelecionada.setForeground(textDesSelecionadoColor);
                    labelSelecionada.getParent().setBackground(isDiaFeriado(dat2) ? feriadoColor : (i) % 7 == 0 ? fdsColor :(i+1) % 7 == 0 ? fdsColor: dsColor);
                    labelSelecionada = null;
                }
            }
            tmpColor = dsColor;
            dat2 = dat2.plusDays(1);
            cont++;
            cont2++;
        }
    }

    /**
     * Preenche meses.
     */
    private void preencheMes() {

        for (int i = 0; i < meses.length; i++) {
            meses[i].addMouseListener(new MouseAdapter() {

                @Override
                public void mouseClicked(MouseEvent e) {

                    e.getComponent().getParent().setBackground(new Color(242, 241, 240));
                    String mesnome = ((JLabel) e.getComponent()).getText();
                    mesData = numeroMes(mesnome);
                    LocalDate dateTmp = new LocalDate(anoData, mesData, 1);
                    preencheDia(dateTmp);
                    labelCalendar.removeAll();
                    mes.setText(mesnome);
                    labelCalendar.add(mes);
                    labelCalendar.add(ano);
                    labelCalendar.updateUI();
                    diasPainel.removeAll();
                    diasPainel.add(caledarioInternoa);
                    diasPainel.add(calendarInterb);
                    diasPainel.updateUI();

                }

                @Override
                public void mouseEntered(MouseEvent e) {
                    e.getComponent().getParent().setBackground(new Color(151, 206, 227));
                }

                @Override
                public void mouseExited(MouseEvent e) {
                    e.getComponent().setForeground(textDesSelecionadoColor);
                    e.getComponent().getParent().setBackground(new Color(242, 241, 240));
                }

                @Override
                public void mouseReleased(MouseEvent e) {
                    e.getComponent().getParent().setBackground(new Color(242, 241, 240));
                    e.getComponent().setForeground(textDesSelecionadoColor);
                }

                @Override
                public void mousePressed(MouseEvent e) {
                    e.getComponent().getParent().setBackground(selecionadoColor);
                    e.getComponent().setForeground(textSelecionadoColor);
                }
            });
        }
    }

